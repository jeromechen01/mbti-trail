  /**
   * 模板变量替换：把 "你和{nameA}..." 里的 {nameA} 换成实际值
   */
  function render(template, vars) {
    if (!template || !vars) return template || '';
    return template.replace(/\{(\w+)\}/g, function (_, k) {
      return (vars[k] != null) ? vars[k] : '';
    });
  }

  /**
   * 从 MBTI 关系类型 + matchResult 中提取所有 render 所需变量。
   */
  function buildTemplateVars(matchResult) {
    const A = matchResult.personA || {};
    const B = matchResult.personB || {};
    const mbti = matchResult.mbtiResult || {};
    const bazi = matchResult.baziResult || {};
    const astro = matchResult.astroResult || {};

    const familyA = (function () {
      if (!A.mbti) return '';
      const mid = A.mbti.slice(1, 3);
      if (mid === 'NT') return FAMILY_NAMES.NT;
      if (mid === 'NF') return FAMILY_NAMES.NF;
      if (mid[0] === 'S' && A.mbti[3] === 'J') return FAMILY_NAMES.SJ;
      if (mid[0] === 'S' && A.mbti[3] === 'P') return FAMILY_NAMES.SP;
      return '';
    })();

    const elementCN = { wood: '木', fire: '火', earth: '土', metal: '金', water: '水' };

    return {
      nameA: A.nickname || '你',
      nameB: B.nickname || 'TA',
      traitA: typeTraits[A.mbti] || '',
      traitB: typeTraits[B.mbti] || '',
      family: familyA,
      element: (A.bazi && A.bazi.dayMaster) ? elementCN[A.bazi.dayMaster.element] : '',
      elementB: (B.bazi && B.bazi.dayMaster) ? elementCN[B.bazi.dayMaster.element] : '',
      sunA: (A.astro && A.astro.sun) ? A.astro.sun.sign : '',
      sunB: (B.astro && B.astro.sun) ? B.astro.sun.sign : ''
    };
  }

  /**
   * 渲染 MBTI 关系类型描述。
   */
  function renderMbtiDesc(matchResult) {
    const mbti = matchResult.mbtiResult;
    if (!mbti || !mbti.type) return '';
    const tpl = mbtiTypeDesc[mbti.type];
    if (!tpl) return '';
    return render(tpl, buildTemplateVars(matchResult));
  }

  /**
   * 渲染五行关系叙事。relation: 'same' | 'A_gen_B' | 'B_gen_A' | 'A_ke_B' | 'B_ke_A'
   */
  function renderBaziNarrative(matchResult) {
    const bazi = matchResult.baziResult;
    if (!bazi) return '';
    const relType = bazi.relationType || 'love';

    // 把 5 种关系压缩为 3 种 key
    let key;
    if (bazi.relation === 'same') key = 'same';
    else if (bazi.relation === 'A_gen_B' || bazi.relation === 'B_gen_A') key = 'sheng';
    else if (bazi.relation === 'A_ke_B' || bazi.relation === 'B_ke_A') key = 'ke';
    else return '';

    // marriage 复用 love 模板
    const relSuffix = (relType === 'marriage') ? 'love' : relType;
    const tplKey = key + '_' + relSuffix;
    const tpl = baziNarrative[tplKey];
    if (!tpl) return '';
    return render(tpl, buildTemplateVars(matchResult));
  }

  /**
   * 渲染十维度某一维的描述。带 fallback 规则：精确组合 → 同 MBTI 类型 → 维度兜底。
   */
  function renderDimension(dimName, matchResult) {
    const dim = tenDimensions[dimName];
    if (!dim) return '';
    const mbti = matchResult.mbtiResult || {};
    const bazi = matchResult.baziResult || {};
    const astro = matchResult.astroResult || {};

    // 拼组合 key
    const mbtiKey = ({ '互补型': '互补', '同频型': '同频', '挑战型': '挑战', '冲突型': '冲突' })[mbti.type] || '同频';
    const baziKey = (function () {
      if (!bazi.relation || bazi.relation === 'same') return '同';
      if (bazi.relation === 'A_gen_B' || bazi.relation === 'B_gen_A') return '生';
      if (bazi.relation === 'A_ke_B' || bazi.relation === 'B_ke_A') return '克';
      return '同';
    })();
    const astroKey = (astro.sunRelation === 'same_element') ? '同象' : '异象';

    const exactKey = mbtiKey + '_' + baziKey + '_' + astroKey;
    if (dim[exactKey]) return render(dim[exactKey], buildTemplateVars(matchResult));

    // fallback 1: 同 MBTI 类型其他变体
    for (const k of Object.keys(dim)) {
      if (k.startsWith(mbtiKey + '_')) return render(dim[k], buildTemplateVars(matchResult));
    }
    // fallback 2: 任意第一条
    const firstKey = Object.keys(dim)[0];
    if (firstKey) return render(dim[firstKey], buildTemplateVars(matchResult));

    return '';
  }

